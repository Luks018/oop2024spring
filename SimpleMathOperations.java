public class SimpleMathOperations {
    public static void main(String[] args) {
        float a = 2;
        float b = 10;
        float res1 = a + b;
        float res2 = a - b;
        float res3 = a * b;
        float res4 = a / b;
        System.out.println("a+b="+res1);
        System.out.println("a-b="+res2);
        System.out.println("a*b="+res3);
        System.out.println("a/b="+res4);
    }
}
